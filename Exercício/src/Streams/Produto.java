package Streams;

public class Produto {
	final String nome;
	final Double preco;
	final Double desconto;
	final Double frete;
	
	
	public Produto(String nome, Double preco, Double desconto, Double frete) {

		this.nome = nome;
		this.preco = preco;
		this.desconto = desconto;
		this.frete = frete;
	}
	
}